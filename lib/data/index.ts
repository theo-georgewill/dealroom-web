// Dashboard Metrics
export function getDashboardMetrics(): DashboardMetrics {
  const activeDeals = MOCK_DEALS.filter(d => ['In Progress', 'Due Diligence', 'Document Review', 'On Hold'].includes(d.status)).length;
  const closingThisMonth = MOCK_DEALS.filter(d => {
    const closeDate = new Date(d.closingDate);
    const now = new Date();
    return closeDate.getMonth() === now.getMonth() && closeDate.getFullYear() === now.getFullYear();
  }).length;
  const totalValue = MOCK_DEALS.reduce((sum, d) => sum + d.dealValue, 0);

  return {
    totalDeals: MOCK_DEALS.length,
    activeDeals,
    closingThisMonth,
    totalDealValue: totalValue,
    currency: 'NGN',
  };
}

